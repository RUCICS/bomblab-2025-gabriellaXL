A = [[827, 899, 485],
     [68, 445, 264]]

B = [[493, 887],
     [629, 678],
     [802, 891]]

# 计算矩阵乘法
C = [[0, 0],
     [0, 0]]

for i in range(2):  # A的行
    for j in range(2):  # B的列
        for k in range(3):  # 内积
            C[i][j] += A[i][k] * B[k][j]

print("计算结果:")
print(f"C[0][0] = {C[0][0]}")  # 1362152
print(f"C[0][1] = {C[0][1]}")  # 1775206  
print(f"C[1][0] = {C[1][0]}")  # 525157
print(f"C[1][1] = {C[1][1]}")  # 597250

print("\nphase_2 答案:")
print(f"{C[0][0]} {C[0][1]} {C[1][0]} {C[1][1]}")